    public String comprobarCero(float resultado){
        String respuesta;
        respuesta = Float.toString(resultado);
        if (resultado % 1 == 0){
            respuesta = respuesta.substring(0, respuesta.length()-2);
        }
        return respuesta;
    }
    
   public String comprobarRaizNegativa(float numero){
    if (numero < 0){
        return "imaginario";
    } else {
        float raiz = (float) Math.sqrt(numero);
        return comprobarCero(raiz);
    }
}

